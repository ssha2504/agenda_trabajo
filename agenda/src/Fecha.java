public class Fecha {
    int dia;
    int mes;
    int año;

    public int getAño() {
        return año;
    }
    public int getDia() {
        return dia;
    }
    public int getMes() {
        return mes;
    }
    public void setAño(int año) {
        this.año = año;
    }
    public void setDia(int dia) {
        this.dia = dia;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }

}
