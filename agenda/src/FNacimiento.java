public class FNacimiento {
    int dia;
    int mes;
    int ano;

    public int getAno() {
        return ano;
    }
    public int getDia() {
        return dia;
    }
    public int getMes() {
        return mes;
    }
    public void Ano(int ano) {
        this.ano = ano;
        this.mes = mes;
        this.dia = dia;
    }


}
